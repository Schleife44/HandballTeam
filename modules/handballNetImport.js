/**
 * Helper to apply "auto-going" status to all roster players who haven't responded yet.
 * @param {Object} event The calendar event to update
 */
export function applyAutoGoing(event) {
    if (!event.responses) event.responses = {};
    const roster = spielstand.roster || [];
    const assignments = spielstand.rosterAssignments || {};
    
    roster.forEach(p => {
        const name = p.name || `Spieler #${p.number}`;
        const assignedUid = Object.keys(assignments).find(u => assignments[u] === name);
        const key = assignedUid || `manual_${name.replace(/\s+/g, '_')}`;
        
        // Only set status if they haven't voted or if current status is auto-generated
        if (!event.responses[key] || event.responses[key].isAutoGenerated) {
            event.responses[key] = { 
                status: 'going', 
                name, 
                updatedAt: Date.now(), 
                isAutoGenerated: true 
            };
        }
    });
}

