/**
 * Extracts the unique team ID from a handball.net team page URL.
 * Supports URLs like:
 *   https://www.handball.net/mannschaften/handball4all.westfalen.1289886/
 *   https://www.handball.net/mannschaften/handball4all.westfalen.1289886/spielplan
 * @param {string} url
 * @returns {string|null} The team ID (e.g. "handball4all.westfalen.1289886") or null
 */
export function parseTeamUrl(url) {
    try {
        const match = url.match(/mannschaften\/(handball4all\.[^/]+)/i);
        if (match && match[1]) return match[1];
        return null;
    } catch (e) {
        return null;
    }
}

/**
 * Fetches team summary + table (which includes the tournament ID) from handball.net API.
 * @param {string} teamId  e.g. "handball4all.westfalen.1289886"
 * @returns {Promise<Object>} The full response including teamSummary and table info
 */
export async function fetchTeamTable(teamId) {
    const url = `https://www.handball.net/a/sportdata/1/widgets/team/${teamId}/table`;
    return await fetchWithProxy(url, { json: true });
}

/**
 * Fetches the full season schedule for a given tournament ID from handball.net API.
 * @param {string} tournamentId  e.g. "handball4all.westfalen.k10_m-kl_wfia"
 * @returns {Promise<Array>} Array of GameSummary objects
 */
export async function fetchTournamentSchedule(tournamentId) {
    const url = `https://www.handball.net/a/sportdata/1/widgets/tournament/${tournamentId}/schedule`;
    const data = await fetchWithProxy(url, { json: true });
    if (!data || !data.schedule || !data.schedule.data) {
        throw new Error('Ungültiges Spielplan-Format von handball.net');
    }
    return data.schedule.data;
}

/**
 * Syncs all games from handball.net that involve the user's team into the calendar.
 * - Filters only games where the configured team is home or away
 * - Creates calendar events with type "game", title, date, time, and location
 * - Skips games that are already in the calendar (duplicate detection via hnetGameId)
 *
 * @param {string} teamId  e.g. "handball4all.westfalen.1289886"
 * @returns {Promise<{added: number, skipped: number, tournamentName: string}>}
 */
export async function syncScheduleToCalendar(teamId, rules = {}) {
    // Step 1: Get team info & tournament ID
    const tableData = await fetchTeamTable(teamId);
    if (!tableData || !tableData.table || !tableData.table.tournament) {
        throw new Error('Konnte Turnier-ID nicht ermitteln. Bitte überprüfe den Team-Link.');
    }
    const tournamentId = tableData.table.tournament.id;
    const tournamentName = tableData.table.tournament.name || 'Liga';

    // Store tournament ID for dashboard table
    spielstand.settings.handballNetTournamentId = tournamentId;
    spielstand.settings.handballNetTeamId = teamId;

    // Step 2: Fetch full schedule
    const allGames = await fetchTournamentSchedule(tournamentId);

    // Step 3: Filter only this team's games
    const myGames = allGames.filter(g =>
        g.homeTeam?.id === teamId || g.awayTeam?.id === teamId
    );

    if (myGames.length === 0) {
        throw new Error(`Keine Spiele für dieses Team in der Ligatabelle gefunden.`);
    }

    // Step 4: Prepare calendar & sync
    if (!spielstand.calendarEvents) spielstand.calendarEvents = [];

    // Build DUAL duplicate index:
    // 1. By hnetGameId (for previously synced games)
    // 2. By date (YYYY-MM-DD) for manually entered games — prevents double entries
    const existingHnetIds = new Set(
        spielstand.calendarEvents
            .filter(e => e.hnetGameId)
            .map(e => e.hnetGameId)
    );
    const existingGameDates = new Set(
        spielstand.calendarEvents
            .filter(e => e.type === 'game' || e.type === 'spiel')
            .map(e => e.date?.slice(0, 10))
            .filter(Boolean)
    );

    let added = 0;
    let skipped = 0;
    let merged = 0;

    for (const game of myGames) {
        const hnetId = game.id;
        const isHome = game.homeTeam?.id === teamId;
        const opponent = isHome ? game.awayTeam?.name : game.homeTeam?.name;
        const startMs = game.startsAt;
        const startDate = new Date(startMs);
        const dateStr = startDate.toISOString().slice(0, 10); // YYYY-MM-DD
        const timeStr = startDate.toLocaleTimeString('de-DE', { hour: '2-digit', minute: '2-digit' });

        const eventRules = {
            requireReason: rules.requireReason || false,
            deadlineHours: rules.deadlineHours || 0,
            defaultStatus: rules.autoGoing ? 'going' : 'none'
        };

        // Check 1: Already synced via hnetGameId
        if (existingHnetIds.has(hnetId)) {
            const existingEvent = spielstand.calendarEvents.find(e => e.hnetGameId === hnetId);
            if (existingEvent) {
                // Update rules even for existing games
                existingEvent.rules = eventRules;
                // If autoGoing is requested, apply it to those who haven't responded yet
                if (rules.autoGoing) {
                    applyAutoGoing(existingEvent);
                }
            }
            skipped++;
            continue;
        }

        // Check 2: A game entry already exists on this date (manual entry) → tag it instead of duplicating
        if (existingGameDates.has(dateStr)) {
            // Find the existing event and tag it with the hnetGameId so future syncs skip it
            const existingEvent = spielstand.calendarEvents.find(e =>
                (e.type === 'game' || e.type === 'spiel') &&
                e.date?.slice(0, 10) === dateStr &&
                !e.hnetGameId
            );
            if (existingEvent) {
                existingEvent.hnetGameId = hnetId;
                existingEvent.rules = eventRules; // Apply rules to merged event too
                if (rules.autoGoing) {
                    applyAutoGoing(existingEvent);
                }
                existingHnetIds.add(hnetId);
                merged++;
                continue;
            }
        }

        const location = game.field?.name && game.field?.city
            ? `${game.field.name}, ${game.field.city}`
            : (game.field?.name || '');

        const title = isHome
            ? `${tableData.teamSummary?.name || 'Wir'} vs. ${opponent}`
            : `${opponent} vs. ${tableData.teamSummary?.name || 'Wir'} (A)`;

        const calendarEvent = {
            id: `hnet_${hnetId}_${Date.now()}`,
            hnetGameId: hnetId,
            title: title,
            date: dateStr,
            time: timeStr,
            type: 'game',
            location: location,
            isAuswaerts: !isHome,
            opponent: opponent,
            notes: game.state === 'Post' && game.homeGoals != null
                ? `Ergebnis: ${game.homeGoals}:${game.awayGoals}`
                : '',
            rules: eventRules,
            responses: {}, // Empty start
            createdAt: new Date().toISOString()
        };

        if (rules.autoGoing) {
            applyAutoGoing(calendarEvent);
        }

        spielstand.calendarEvents.push(calendarEvent);
        existingHnetIds.add(hnetId);
        existingGameDates.add(dateStr);
        added++;
    }

    speichereSpielstand();

    return { added, skipped, merged, total: myGames.length, tournamentName };
}



/**
 * Parses the game ID from a handball.net URL.
 * URL Example: https://www.handball.net/spiele/handball4all.westfalen.8348616/spielbericht
 */
export function parseGameId(url) {
    try {
        const parts = url.split('/');
        // The ID is usually the part before 'spielbericht' or 'spielplan'
        for (let i = 0; i < parts.length; i++) {
            if (parts[i] === 'spiele' && parts[i + 1]) {
                return parts[i + 1].split('?')[0];
            }
        }
        return null;
    } catch (e) {
        return null;
    }
}

